import numpy as np

def hello_world() -> str:
    """
    Implement a function that returns Hello World!
    """
    #raise NotImplementedError()
    return "Hello World!"


def add_two(number):
    """
    Adds two to `number`
    """
    #raise NotImplementedError()
    return number+2


def zero_array(N):
    """
    Returns a double type ndarray of length N filled with zeros 
    """
    #raise NotImplementedError()
    return np.zeros(N, dtype=np.float64)
